#!/bin/sh
## Tail multiple logs.

set -E

###############################################################################
# Environment
###############################################################################

LOGS="
  /var/log/tor/notice.log
  /var/log/bitcoin/debug.log
  /var/log/lightningd.log
  /var/log/cl-rest.log
"

###############################################################################
# Methods
###############################################################################

log_string() {
  echo "$(
    for log in "$LOGS"; do
      if [ -e "$log" ]; then echo $log; fi
    done | tr '\n' ' '
  )"
}

###############################################################################
# Script
###############################################################################

tail -f `log_string`